def inbox_json() -> str:
    """Generate .ai_mail.local/inbox.json — empty project mailbox structure."""
    return (
        json.dumps(
            {
                "mailbox": "inbox",
                "total_messages": 0,
                "unread_count": 0,
                "messages": [],
            },
            indent=2,
            ensure_ascii=False,
        )
        + "\n"
    )


def with_source(content: str, file_path: Path) -> str:
    """Prepend a source header to AI prompt file content."""
    return f"<!-- Source: {file_path} -->\n{content}"
